<template>
    <div >
         <button class="btn btn-primary ml-4" @click="followUser">Follow</button>
    </div>
</template>

<script>
    export default {
        props: ['userId'],

        mounted() {
            console.log('Follow button mounted.')
        },

        methods:{
            followUser(){
                axios.post('/follow/' + this.userId)
                    .then(response=>{
                        alert(response.data);
                    });
            }
        }
    }
</script>
